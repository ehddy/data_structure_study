from avl import AVL
if __name__ == '__main__':
    t = AVL()
    t.put(75, 'apple')
    t.put(40, 'grape')
    t.put(85, 'lime')
    t.put(20, 'mango')
    t.put(50, 'strawberry')
    t.put(80, 'banana')
    t.put(90, 'cherry')
    t.put(10, 'watermelon')
    t.put(30, 'melon')
    t.put(70, 'plum')
    print('전위순회:\t', end='')
    t.preorder(t.root)
    print('\n중위순회:\t', end='')
    t.inorder(t.root)

    print('\n75와 85 삭제 후:')
    t.delete(75)
    t.delete(85)
    print('전위순회:\t', end='')
    t.preorder(t.root)
    print('\n중위순회:\t', end='')
    t.inorder(t.root)
